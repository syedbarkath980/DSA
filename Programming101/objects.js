/* Objects are the successor of an array,
   that can store multiple types of data within a single name.
It Stores data in Key : Value pair */

let obj = {
    name : "Syed",
    age : 20,
    sgpa : 8.6,
    height : 6.5,
}

//Accessing the elements of an object: Using dot (.) Operator.
console.log(obj.name)
console.log(obj.age)
console.log(obj.sgpa)
console.log(obj.height)

//Or directly accessing the whole object like: 
console.log(obj)


